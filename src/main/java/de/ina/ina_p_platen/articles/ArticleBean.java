package de.ina.ina_p_platen.articles;

public class ArticleBean {

    private String name;
    private int amount;

    public ArticleBean() {

    }

    public String getName() {
        return name;
    }

    public int getAmount() {
        return amount;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
